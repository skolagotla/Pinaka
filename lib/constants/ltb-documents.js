/**
 * Get LTB documents filtered by location
 */
function getLTBDocumentsByLocation(country, province) {
  return LTB_DOCUMENTS.filter(
    doc => doc.country === country && doc.province === province
  );
}

/**
 * Get LTB documents filtered by audience
 */
function getLTBDocumentsByAudience(audience) {
  if (audience === 'all') return LTB_DOCUMENTS;
  return LTB_DOCUMENTS.filter(doc => {
    if (audience === 'landlord') return doc.audience === 'landlord' || doc.audience === 'both';
    if (audience === 'tenant') return doc.audience === 'tenant' || doc.audience === 'both';
    return doc.audience === audience;
  });
}

/**
 * Search LTB documents
 */
function searchLTBDocuments(query) {
  if (!query) return LTB_DOCUMENTS;
  const lowerQuery = query.toLowerCase();
  return LTB_DOCUMENTS.filter(doc =>
    doc.formNumber.toLowerCase().includes(lowerQuery) ||
    doc.name.toLowerCase().includes(lowerQuery) ||
    (doc.description && doc.description.toLowerCase().includes(lowerQuery)) ||
    (doc.tags && doc.tags.some(tag => tag.toLowerCase().includes(lowerQuery)))
  );
}

